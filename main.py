# Запит користувача на введення 4-х значного числа
number = int(input("Введіть 4-х значне число: "))

# Виведення кожної цифри числа у стовпчик
print(number // 1000)  # перша цифра
print((number % 1000) // 100)  # друга цифра
print((number % 100) // 10)  # третя цифра
print(number % 10)  # четверта цифра
